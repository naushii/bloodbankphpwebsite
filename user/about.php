	<br>
			<p>A <b>Blood Bank</b> is a center where blood gathered as a result of blood donation is stored and preserved for later use in blood transfusion. The term "blood blank" typically refer to a division of a hospital where the storage of blood product occurs and where proper testing is performed(to reduce the risk of transfusion related adverse events.) However,it sometimes refers to a collection center, and indeed some hospitals also perform collection.Blood banking includes tasks related to blood collection,processing,testing,separation,and storage.</p>
			<p>for blood donation agencies in various countries,see List of blood donation agencies and List of blood donation agencies in the United States. </p>
			<p>Routine blood storage is 42 days or 6 weeks for stored packed red blood cells(also called "StRBC" or "pRBC"), by far the most commonly transfused blood product, and involves refrigeration but usually not freezing.There has been increasing controversy about whether a given product unit's age is a factor in transfusion efficacy,specifically on whether "older" blood directly or indirectly increases risks of complications. Studies have not been consistent on answering this question, with some showing that older blood is indeed less effective but with others showing no such difference;nevertheless,as storage time remains the only available way to estimate quality status or loss, a <i>first-in-first-out</i> inventory management approach is standard presently.</p>
